# Criando de listas
notas = [] # vazia
alunos = ["Rubens", "Gabriel", "Yago", "Lucas"] # preenchida

# Tamanho da lista
print(notas)
print(len(notas))

print(alunos)
print(len(alunos))

#  Acessando elementos - Pela posição:
print("\n1. ACESSANDO ELEMENTOS: ")
print(alunos[0]) # Primeiro aluno: "Rubens"
print(alunos[1]) # Segundo aluno: "Gabriel"

# Acessando elementos - Índices negativos:
print("\n2. ACESSANDO ELEMENTOS UTILIZANDO ÍNDICIES REVERSOS: ")
print(alunos[-1]) # Último: "Lucas"
print(alunos[-2]) # Penúltimo: "Yago"


